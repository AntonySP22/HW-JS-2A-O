var num1, num2;

num1 = parseInt(prompt("Ingrese un numero: ")); 
num2 = parseInt(prompt("Ingrese un numero: ")); 

//Suma
var suma = num1+num2;

document.write("La suma de los numeros ingresados es: "+ suma + "<br>");

//Resta
var resta = num1 - num2;

document.write("La resta de los numeros ingresados es: "+ resta + "<br>");

//Multiplicacion
var multi = num1 * num2;

document.write("La multiplicacion de los numeros ingresados es: "+ multi + "<br>");
//Division
var divi = num1 / num2;

document.write("La division de los numeros ingresados es: "+ divi + "<br>");